  Future<bool> load() async {
    _localizedStrings =
        _translations[locale.languageCode] ?? _translations['id']!;
    return true;
  }

  String translate(String key, {Map<String, String>? params}) {
    String value = _localizedStrings[key] ?? key;
    if (params != null) {
      params.forEach((k, v) {
        value = value.replaceAll('{$k}', v);
      });
    }
    return value;
  }

  static String getCategoryName(BuildContext context, String categoryName) {
    final keys = {
      'Makanan & Minuman': 'cat_food_drink',
      'Transportasi': 'cat_transport',
      'Hiburan': 'cat_entertainment',
      'Lainnya': 'cat_others',
      'Gaji': 'cat_salary',
    };

    final key = keys[categoryName];
    if (key != null) {
      return AppLocalizations.of(context).translate(key);
    }
    return categoryName;
  }
